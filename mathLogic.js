/**
 * _renderMCButtons(options)
 * ──────────────────────────
 * PRIVATE — injects 4 multiple-choice buttons into #mc-fields for the current round.
 *
 * Each button's onclick passes two arguments to the platform bridge:
 *   · opt.isCorrect  — grading signal for evaluateGuess()
 *   · this           — the DOM element, so evaluateGuess can colour it red/green
 *
 * WHY pass `this`?
 *   The platform needs to highlight the clicked button immediately without
 *   searching the DOM for which one was tapped. Passing the element directly
 *   is faster and eliminates a querySelector call.
 *
 * @param {Array<{text: string, isCorrect: boolean}>} options — The 4 answer choices
 */
function _renderMCButtons(options) {
    const container = document.getElementById('mc-fields');
    container.classList.remove('hidden');
    container.innerHTML = options.map(opt => `
        <button class="mc-btn" onclick="evaluateGuess(${opt.isCorrect}, this)">
            ${opt.text}
        </button>`).join('');
}

/**
 * _startRoundTimer(timeLimit, onExpire)
 * ──────────────────────────────────────
 * PRIVATE — starts the countdown timer for the current round.
 *
 * Each tick:
 *   · Updates the #timer-display element.
 *   · At the halfway point (Easy only): fades one wrong button to give a hint.
 *   · Plays sfxTick for urgency in the last 3 seconds.
 *   · At 0: clears itself and calls onExpire() (evaluates as incorrect).
 *
 * The timer ID is stored in state.timerId so it can be cancelled by
 * evaluateGuess() or evaluateMultiplayerRound() when the player answers
 * before time runs out.
 *
 * @param {number}   timeLimit — Seconds for this round (set by difficulty level)
 * @param {Function} onExpire  — Callback invoked when the timer hits 0
 */
function _startRoundTimer(timeLimit, onExpire) {
    let timeLeft = timeLimit;
    const display = document.getElementById('timer-display');
    if (display) display.innerText = timeLeft;

    state.timerId = setInterval(() => {
        timeLeft--;
        if (display) display.innerText = timeLeft;

        // Auditory urgency cue in the last 3 seconds
        if (timeLeft <= 3 && timeLeft > 0) {
            sfxTick.currentTime = 0;
            sfxTick.play().catch(() => {});
        }

        // Easy-mode hint: fade one wrong button at the halfway point
        if (state.gameState.level === 'easy' && timeLeft === Math.floor(timeLimit / 2)) {
            const wrongBtns = [...document.querySelectorAll('#mc-fields .mc-btn')]
                .filter(btn => !btn.onclick.toString().includes('true'));
            if (wrongBtns.length > 0) {
                wrongBtns[Math.floor(Math.random() * wrongBtns.length)].style.opacity = '0.2';
            }
        }

        if (timeLeft <= 0) {
            clearInterval(state.timerId);
            onExpire();
        }
    }, 1000);
}


