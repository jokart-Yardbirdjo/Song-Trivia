/**
 * evaluateGuess(isCorrect, clickedBtn)
 * ──────────────────────────────────────
 * EXPORTED — called by app.js window.evaluateGuess() delegation.
 * Handles solo scoring only. evaluateMultiplayerRound() handles host scoring.
 *
 * FLOW:
 *   1. Guard against double-evaluation (state.isProcessing flag).
 *   2. Stop the timer immediately.
 *   3. Stop background music (if any was playing).
 *   4. Disable all MC buttons to prevent further input.
 *   5. Colour the clicked button: red if wrong, green if correct.
 *   6. Calculate points (time-based + streak bonus + double-round multiplier).
 *   7. Handle Sudden Death elimination if wrong.
 *   8. Update #feedback and #score-board.
 *   9. Advance to next round after a 2s pause.
 *
 * SCORING FORMULA (standard modes):
 *   basePoints = timeRemaining / timeLimit × 200   (max ~200 per round)
 *   streakBonus = +50 every 3rd consecutive correct
 *   doubleRound = total × 2
 *
 * SUDDEN DEATH:
 *   A wrong answer triggers endGameSequence() directly.
 *   sdRoundsAlive is the persistent stat (not rawScores).
 *
 * @param {boolean}          isCorrect  — Whether the tapped button was correct
 * @param {HTMLElement|null} clickedBtn — The button the player tapped (for colouring)
 */
export function evaluateGuess(isCorrect, clickedBtn = null) {
    // ── Guard: prevent double-fire from rapid taps or timer+button race ──
    if (state.isProcessing) return;
    state.isProcessing = true;

    clearInterval(state.timerId);
    bgm.pause();
    bgm.currentTime = 0;

    // ── Disable all buttons to lock input ──
    document.querySelectorAll('#mc-fields .mc-btn').forEach(b => { b.disabled = true; });

    // ── Visual feedback on the clicked button ──
    if (clickedBtn) clickedBtn.classList.add(isCorrect ? 'correct' : 'wrong');

    // ── Reveal the correct button (green) so players see it after wrong answers ──
    document.querySelectorAll('#mc-fields .mc-btn').forEach(btn => {
        if (btn.onclick?.toString().includes('true')) btn.classList.add('correct');
    });

    const isDouble  = !isSuddenDeath() && state.doubleRounds.includes(state.curIdx);
    const feedback  = document.getElementById('feedback');
    let   roundPts  = 0;

    if (isCorrect) {
        state.streaks[0]++;
        state.sdRoundsAlive++;

        // Time-based scoring: reward speed relative to the time limit
        const timerEl   = document.getElementById('timer-display');
        const timeLeft  = parseInt(timerEl?.innerText || 0, 10);
        roundPts        = Math.round(100 + (timeLeft / state.timeLimit) * 100);

        const streakBonus = (state.streaks[0] > 0 && state.streaks[0] % 3 === 0);
        if (streakBonus) roundPts += 50;
        if (isDouble)    roundPts  = Math.round(roundPts * 2);

        state.rawScores[0] += roundPts;
        sfxCheer.currentTime = 0; sfxCheer.play().catch(() => {});

        const streakMsg = streakBonus
            ? `<div style="color:var(--p3); font-size:0.9rem; margin-top:4px; font-weight:bold;">🔥 ${state.streaks[0]}-streak! +50 bonus</div>`
            : '';
        const doubleMsg = isDouble
            ? `<div style="color:#f39c12; font-size:0.9rem; font-weight:bold; margin-top:4px;">⭐ 2X BONUS ROUND!</div>`
            : '';

        if (feedback) feedback.innerHTML = `
            <div style="color:var(--success); font-size:1.4rem; font-weight:bold; margin-top:10px;">
                ✅ CORRECT! +${roundPts}
            </div>${streakMsg}${doubleMsg}`;

    } else {
        state.streaks[0] = 0;
        sfxBuzzer.currentTime = 0; sfxBuzzer.play().catch(() => {});

        if (isSuddenDeath()) {
            // ── Sudden Death: wrong answer = game over ──
            if (feedback) feedback.innerHTML = `
                <div style="color:var(--fail); font-size:2rem; font-weight:900; margin-top:15px;">💀 ELIMINATED!</div>
                <div style="color:var(--fail); font-size:1.1rem; margin-top:5px;">You survived ${state.sdRoundsAlive} rounds.</div>`;
            setTimeout(endGameSequence, 2500);
            return; // Skip the normal advance
        }

        if (feedback) feedback.innerHTML = `
            <div style="color:var(--fail); font-size:1.4rem; font-weight:bold; margin-top:10px;">
                ❌ INCORRECT
            </div>`;
    }

    // ── Update the score pill ──
    document.getElementById('score-board').innerHTML = _buildScoreBoard();

    // ── Advance after 2s feedback window ──
    state.curIdx++;
    setTimeout(nextRound, 2000);
}


