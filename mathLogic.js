/**
 * endGameSequence()
 * ──────────────────
 * PRIVATE — called when all rounds are complete (or Sudden Death eliminates players).
 * Transitions the play-screen → final-screen and writes final stat updates.
 *
 * STAT TRACKING:
 *   · Increments gamesPlayed on every run.
 *   · Updates highScore if the current score beats the record.
 *   · Updates bestStreak from state.streaks[0].
 *   · Updates suddenDeathRecord (rounds survived) only in Sudden Death mode.
 *
 * MULTIPLAYER:
 *   · Host writes phase: 'gameover' to Firebase, triggering client finale screens.
 *   · Reads the final leaderboard from Firebase to build the podium.
 *
 * SOLO FINALE HTML:
 *   Renders a gradient score card with mode, score, and a new-record badge if earned.
 */
function endGameSequence() {
    clearInterval(state.timerId);
    bgm.pause();
    bgm.currentTime = 0;

    document.getElementById('play-screen').classList.add('hidden');
    document.getElementById('final-screen').classList.remove('hidden');
    document.getElementById('final-subtitle').innerText = isSuddenDeath()
        ? "💀 Sudden Death — Rounds Survived"
        : "Speed + Accuracy Scoring";

    // ── Stat persistence ──
    const s = state.userStats.fast_math || {};
    s.gamesPlayed = (s.gamesPlayed || 0) + 1;

    const finalScore = state.rawScores[0] || 0;
    const newRecord  = finalScore > (s.highScore || 0);
    if (newRecord) s.highScore = finalScore;

    const finalStreak = state.streaks[0] || 0;
    if (finalStreak > (s.bestStreak || 0)) s.bestStreak = finalStreak;

    if (isSuddenDeath() && (state.sdRoundsAlive || 0) > (s.suddenDeathRecord || 0)) {
        s.suddenDeathRecord = state.sdRoundsAlive;
    }

    state.userStats.fast_math = s;
    localStorage.setItem('yardbirdPlatformStats', JSON.stringify(state.userStats));

    // ── Multiplayer finale (host only) ──
    if (state.isMultiplayer && state.isHost) {
        db.ref(`rooms/${state.roomCode}/hostState`).set({ phase: 'gameover' });

        db.ref(`rooms/${state.roomCode}/players`).once('value', snap => {
            const players = snap.val() || {};
            const results = Object.keys(players)
                .map(pid => ({ name: players[pid].name, score: players[pid].score || 0 }))
                .sort((a, b) => b.score - a.score);

            db.ref(`rooms/${state.roomCode}/finalLeaderboard`).set(results);

            let podium = `<div style="text-align:left; background:var(--surface); padding:15px; border-radius:12px; border:2px solid var(--border-light);">`;
            results.forEach((p, i) => {
                const medal = ['🥇', '🥈', '🥉'][i] || '👏';
                podium += `<div style="padding:6px 0; font-size:1.1rem; font-weight:bold;">
                    ${medal} ${p.name} — <span style="color:var(--primary)">${p.score} pts</span>
                </div>`;
            });
            podium += `</div>`;
            document.getElementById('winner-text').innerHTML = podium;
        });
        return;
    }

    // ── Solo finale ──
    const sdLine = isSuddenDeath()
        ? `<div style="font-size:1rem; color:var(--fail); margin-top:8px;">💀 Survived: ${state.sdRoundsAlive} rounds</div>`
        : '';
    const recordLine = newRecord
        ? `<div style="color:#f39c12; font-size:0.9rem; margin-top:6px; font-weight:bold;">🏆 NEW HIGH SCORE!</div>`
        : '';

    document.getElementById('winner-text').innerHTML = `
        <div style="background:linear-gradient(135deg, var(--primary), var(--p2)); border-radius:16px; padding:20px; color:#fff;">
            <div style="font-size:2.2rem; font-weight:900;">${finalScore}</div>
            <div style="font-size:0.9rem; opacity:0.85; text-transform:uppercase; letter-spacing:1px;">
                ${state.gameState.mode?.replace('_', ' ')} · ${state.gameState.level?.replace('_', ' ')}
            </div>
            ${sdLine}${recordLine}
        </div>`;
}


