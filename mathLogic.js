/**
 * startGame()
 * ────────────
 * EXPORTED — the platform's entry point into this cartridge.
 * Called by app.js when the player taps "Start Game" or "Start Multiplayer".
 *
 * RESPONSIBILITIES:
 *   1. Garbage-collect state left by other cartridges (songs, pool, matchHistory).
 *   2. Set time limits based on chosen difficulty level.
 *   3. Generate double-round indices (every 5 rounds, one round is ×2 score).
 *   4. Initialize score and streak arrays for solo or multiplayer player count.
 *   5. Transition the UI from setup-screen → play-screen.
 *   6. Kick off the first round via nextRound().
 *
 * DOUBLE ROUND LOGIC:
 *   For every block of 5 rounds, exactly one random round is flagged as a
 *   double-point round. This gives players a surprise score boost mid-game
 *   without being predictable. Sudden Death skips this since score is irrelevant.
 *
 * SUDDEN DEATH NOTE:
 *   sdRoundsAlive is the primary stat tracked during Sudden Death play.
 *   It replaces the traditional score as the metric displayed on the finale.
 */
export function startGame() {
    // ── Garbage collection: wipe state from other cartridges ──
    state.curIdx       = 0;
    state.songs        = [];
    state.globalPool   = [];
    state.matchHistory = [];
    state.isDailyMode  = false;
    state.numPlayers   = state.isMultiplayer ? state.numPlayers : 1;

    // ── Time limit per difficulty level ──
    if      (state.gameState.level === 'easy')   state.timeLimit = 20;
    else if (state.gameState.level === 'medium') state.timeLimit = 12;
    else if (state.gameState.level === 'hard')   state.timeLimit = 6;
    else                                         state.timeLimit = 10; // Sudden Death

    state.maxRounds    = state.gameState.rounds;
    state.sdRoundsAlive = 0;

    // ── Double-round generation (skip for Sudden Death) ──
    state.doubleRounds = [];
    if (!isSuddenDeath()) {
        for (let i = 0; i < state.maxRounds; i += 5) {
            const min = i === 0 ? 2 : i;
            const max = Math.min(i + 4, state.maxRounds - 1);
            if (min <= max) {
                state.doubleRounds.push(Math.floor(Math.random() * (max - min + 1)) + min);
            }
        }
    }

    // ── Initialize per-player score and streak arrays ──
    state.curIdx    = 0;
    state.rawScores = new Array(state.numPlayers).fill(0);
    state.streaks   = new Array(state.numPlayers).fill(0);

    // ── UI transition: setup → play ──
    document.getElementById('setup-screen').classList.add('hidden');
    document.getElementById('play-screen').classList.remove('hidden');
    document.querySelectorAll('.header-btn').forEach(btn => btn.classList.add('hidden'));

    // Hide elements that belong to Song Trivia
    document.getElementById('guess-fields').classList.add('hidden');
    document.getElementById('btn-container').classList.add('hidden');
    document.getElementById('visualizer').classList.add('hidden');
    document.getElementById('reveal-art').style.display = 'none';

    // Solo: render a blank score pill ready to be updated each round.
    if (!state.isHost) {
        document.getElementById('score-board').innerHTML = `
            <div class="score-pill" style="border-color:${colors[0]};">
                <div class="p-name"   style="color:${colors[0]}">SCORE</div>
                <div class="p-pts"    style="color:var(--dark-text)">0</div>
                <div class="p-streak" style="opacity:0">🔥 0</div>
            </div>`;
    }

    nextRound();
}


