/**
 * evaluateMultiplayerRound(players)
 * ───────────────────────────────────
 * EXPORTED — called by app.js window.evaluateMultiplayerRound() delegation.
 * Scores ALL connected players simultaneously based on their Firebase guesses.
 *
 * CALLED BY:
 *   · multiplayer.js startMultiplayerGame() → players listener (all locked)
 *   · _startRoundTimer() onExpire callback  → time's up (host only)
 *
 * SCORING LOGIC:
 *   · Correct MC guess → 100 + (time × 100 / timeLimit) pts  (speed-weighted)
 *   · Wrong/no guess   → 0 points
 *   · Streak bonus     → +50 every 3rd consecutive correct
 *   · Double round     → total × 2
 *
 * SUDDEN DEATH (multiplayer):
 *   ANY player getting it wrong ends the room. The host writes 'gameover'
 *   to Firebase, then calls endGameSequence() after a 4s dramatic pause.
 *   finalizeMultiplayerRound() is still called first so score writes complete.
 *
 * FIREBASE WRITES (delegated to finalizeMultiplayerRound):
 *   · Updates each player's score in /rooms/{code}/players/{id}/score
 *   · Advances currentRound so phones know a new round started
 *   · Clears currentMC so phones drop the old buttons
 *
 * @param {Object} players — Firebase snapshot value: { playerId: { name, guess, score } }
 */
export async function evaluateMultiplayerRound(players) {
    if (state.isProcessing) return;
    state.isProcessing = true;
    if (state.timerId) clearInterval(state.timerId);

    bgm.pause();
    bgm.currentTime = 0;

    const results  = [];
    const isDouble = !isSuddenDeath() && state.doubleRounds.includes(state.curIdx);
    let fbHTML     = `<div style="display:flex; flex-direction:column; gap:6px; margin-bottom:15px; font-weight:bold;">`;

    let fatalMistake = false;
    let guiltyPlayer = "";

    Object.keys(players).forEach((pid, index) => {
        const p       = players[pid];
        const correct = !!(p.guess && p.guess.isMC && p.guess.correct);
        let   roundPts = 0;

        if (correct) {
            state.streaks[index] = (state.streaks[index] || 0) + 1;

            // Speed factor: how quickly did they answer relative to the time limit?
            const speedFactor = (p.guess.time || 0) / state.timeLimit;
            roundPts = Math.round(100 + (speedFactor * 100));

            const streakBonus = (state.streaks[index] > 0 && state.streaks[index] % 3 === 0);
            if (streakBonus) roundPts += 50;
            if (isDouble)    roundPts *= 2;

            fbHTML += `<div style="color:var(--success); font-size:1.1rem;">✅ ${p.name}: +${roundPts}</div>`;
            state.rawScores[index] = (state.rawScores[index] || 0) + roundPts;
        } else {
            state.streaks[index] = 0;
            fbHTML += `<div style="color:var(--fail); font-size:1.1rem;">❌ ${p.name}: 0</div>`;

            // In Sudden Death, any wrong answer kills the room
            if (isSuddenDeath()) {
                fatalMistake = true;
                guiltyPlayer = p.name;
            }
        }

        results.push({ id: pid, newScore: (p.score || 0) + roundPts });
    });

    fbHTML += `</div>`;

    if (fatalMistake) {
        // ── Sudden Death: room eliminated ──
        document.getElementById('feedback').innerHTML = fbHTML + `
            <div style="color:var(--fail); font-size:2rem; font-weight:900; margin-top:15px; line-height:1;">💀 FATAL MISTAKE!</div>
            <div style="color:var(--fail); font-size:1.2rem; margin-top:5px;"><strong>${guiltyPlayer}</strong> eliminated the room!</div>`;

        if (window.finalizeMultiplayerRound) window.finalizeMultiplayerRound(results);
        setTimeout(endGameSequence, 4000);
        return; // Prevent nextRound from firing
    }

    // ── Standard round: show results and advance ──
    document.getElementById('feedback').innerHTML = fbHTML;
    document.getElementById('score-board').innerHTML = _buildScoreBoard();

    state.curIdx++;
    if (window.finalizeMultiplayerRound) window.finalizeMultiplayerRound(results);
    setTimeout(nextRound, 2000);
}


