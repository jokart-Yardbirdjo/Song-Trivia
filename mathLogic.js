/**
 * generateMixedProblem()
 * ───────────────────────
 * PRIVATE — generates a 4-option problem where EACH option uses a DIFFERENT
 * operator. Exactly one evaluates to the target; the other three do not.
 *
 * WHY MIXED IS HARDER:
 *   In single-op mode, players develop one mental strategy (e.g. look for the
 *   right sum). In mixed, every option requires a different cognitive operation.
 *   Players must pattern-switch between addition, subtraction, multiplication,
 *   and division in a single glance.
 *
 * ALGORITHM:
 *   1. Pick a random target in the 10–50 range (smaller = faster to evaluate).
 *   2. Shuffle the four operators.
 *   3. One operator is designated "correct" — build a valid equation for it.
 *   4. The other three operators each get a wrong equation that does NOT
 *      evaluate to the target (loop-guarded with up to 50 attempts each).
 *
 * @returns {Object} — { target: number, options: Array<{text, isCorrect}> }
 */
function generateMixedProblem() {
    const ops      = ['addition', 'subtraction', 'multiplication', 'division'];
    const symMap   = { addition: '+', subtraction: '−', multiplication: '×', division: '÷' };
    const target   = Math.floor(Math.random() * 41) + 10; // 10–50

    // Shuffle operator order so the correct slot is unpredictable.
    const shuffled     = ops.sort(() => 0.5 - Math.random());
    const correctOp    = shuffled[0];
    const correctEq    = makeCorrectEquation(correctOp, target);

    const options = [{
        text: `${correctEq.a} ${symMap[correctOp]} ${correctEq.b}`,
        isCorrect: true
    }];

    // Build one wrong equation per remaining operator.
    for (let i = 1; i < 4; i++) {
        const op = shuffled[i];
        let a, b, result;
        let attempts = 0;

        do {
            attempts++;
            if (op === 'addition') {
                a = Math.floor(Math.random() * 40) + 1;
                b = Math.floor(Math.random() * 40) + 1;
                result = a + b;
            } else if (op === 'subtraction') {
                a = Math.floor(Math.random() * 50) + 11;
                b = Math.floor(Math.random() * 10)  + 1;
                result = a - b;
            } else if (op === 'multiplication') {
                a = Math.floor(Math.random() * 10) + 2;
                b = Math.floor(Math.random() * 10) + 2;
                result = a * b;
            } else {
                b = Math.floor(Math.random() * 9)  + 2;
                a = b * (Math.floor(Math.random() * 10) + 2);
                result = a / b;
            }
        } while (result === target && attempts < 50);

        options.push({ text: `${a} ${symMap[op]} ${b}`, isCorrect: false });
    }

    return {
        target,
        options: options.sort(() => 0.5 - Math.random())
    };
}


