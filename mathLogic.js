/**
 * makeCorrectEquation(op, target)
 * ────────────────────────────────
 * PRIVATE — builds ONE correct equation object for the given operator and target.
 *
 * Design principle: operands are chosen with light randomisation so that the
 * correct answer doesn't always "look" the same (e.g. multiplication always
 * using the smallest factor pair). This prevents players from learning visual
 * shortcuts that bypass number sense.
 *
 * Division note: we construct it backwards — pick a random divisor (b), then
 * derive the dividend (a = target × b) — guaranteeing clean integer division.
 *
 * @param  {string} op     — One of: 'addition' | 'subtraction' | 'multiplication' | 'division'
 * @param  {number} target — The round's target number
 * @returns {Object}       — { a: number, b: number, sym: string }
 */
function makeCorrectEquation(op, target) {
    if (op === 'addition') {
        const a = Math.floor(Math.random() * (target - 2)) + 1;
        return { a, b: target - a, sym: '+' };
    }
    if (op === 'subtraction') {
        const b = Math.floor(Math.random() * Math.max(1, target - 1)) + 1;
        return { a: target + b, b, sym: '−' };
    }
    if (op === 'multiplication') {
        // Gather all valid factor pairs so any pair is equally likely to appear.
        const factors = [];
        for (let i = 2; i <= Math.sqrt(target); i++) {
            if (target % i === 0) factors.push([i, target / i]);
        }
        // Fallback: 1 × target when the target is prime.
        if (factors.length === 0) return { a: 1, b: target, sym: '×' };
        const pair = factors[Math.floor(Math.random() * factors.length)];
        return { a: pair[0], b: pair[1], sym: '×' };
    }
    if (op === 'division') {
        const b = Math.floor(Math.random() * 10) + 2;  // Divisor between 2–11
        return { a: target * b, b, sym: '÷' };
    }
}

/**
 * makeWrongEquations(op, target, correctEq, count)
 * ─────────────────────────────────────────────────
 * PRIVATE — generates `count` deceptive wrong equations for single-operator mode.
 *
 * KEY INSIGHT:
 *   Wrong answers intentionally share surface features with the correct one:
 *   same operator, operands of similar magnitude. This forces players to
 *   actually evaluate — they cannot eliminate wrong answers by type or size alone.
 *
 *   The seen-set ensures no equation pair repeats, and the result is checked
 *   against `target` so no wrong answer accidentally equals the target.
 *
 * SAFETY VALVE:
 *   The 200-attempt ceiling prevents an infinite loop on edge cases (e.g.
 *   a prime target with very few valid operand combinations).
 *
 * @param  {string} op         — Operator used in this round
 * @param  {number} target     — Round's target number (wrong answers must NOT equal this)
 * @param  {Object} correctEq  — The correct equation (used to derive similar operands)
 * @param  {number} count      — Number of wrong equations to generate
 * @returns {Array<Object>}    — Array of { a, b, sym } objects, each != target
 */
function makeWrongEquations(op, target, correctEq, count) {
    const wrongs  = [];
    const seen    = new Set([`${correctEq.a}${correctEq.b}`]);
    let attempts  = 0;

    while (wrongs.length < count && attempts < 200) {
        attempts++;
        let a, b, result;

        if (op === 'addition') {
            // Offset one or both operands by a small delta to stay close to the correct answer.
            const delta = (Math.floor(Math.random() * 4) + 1) * (Math.random() > 0.5 ? 1 : -1);
            a = Math.max(1, correctEq.a + delta);
            b = Math.max(1, correctEq.b + (Math.random() > 0.5 ? delta : -delta));
            result = a + b;

        } else if (op === 'subtraction') {
            const delta = (Math.floor(Math.random() * 5) + 1) * (Math.random() > 0.5 ? 1 : -1);
            a = Math.max(correctEq.b + 2, correctEq.a + delta);  // Keep a > b to avoid negative results
            b = Math.max(1, correctEq.b + (Math.random() > 0.5 ? 1 : -1));
            result = a - b;

        } else if (op === 'multiplication') {
            // Nudge one factor by ±1 or ±2 — keeps numbers visually similar.
            const d = Math.floor(Math.random() * 2) + 1;
            a = Math.max(2, correctEq.a + (Math.random() > 0.5 ? d : -d));
            b = correctEq.b;
            result = a * b;

        } else if (op === 'division') {
            // Change the dividend slightly — same divisor makes it look plausible.
            const d = (Math.floor(Math.random() * 3) + 1) * correctEq.b;
            a = correctEq.a + (Math.random() > 0.5 ? d : -d);
            b = correctEq.b;
            a = Math.max(b + 1, a);  // Ensure dividend > divisor
            result = a / b;
        }

        const key = `${a}${b}`;
        if (seen.has(key) || result === target) continue;
        seen.add(key);

        const symMap = { addition: '+', subtraction: '−', multiplication: '×', division: '÷' };
        wrongs.push({ a, b, sym: symMap[op] });
    }

    return wrongs;
}

/**
 * generateSingleOpProblem(op)
 * ────────────────────────────
 * PRIVATE — assembles a full problem object for a single operator mode.
 *
 * Target ranges per operator:
 *   · Addition / Subtraction  — 10 to 99  (mental arithmetic range)
 *   · Multiplication          — 12 to 144 (times-tables territory)
 *   · Division                — 2 to 12   (small clean quotients)
 *
 * Returns a shuffled options array so the correct answer is never predictably
 * in the same slot each round.
 *
 * @param  {string} op  — Operator for this problem
 * @returns {Object}    — { target: number, options: Array<{text, isCorrect}> }
 */
function generateSingleOpProblem(op) {
    let target;
    if (op === 'addition' || op === 'subtraction') {
        target = Math.floor(Math.random() * 90) + 10;   // 10–99
    } else if (op === 'multiplication') {
        target = Math.floor(Math.random() * 133) + 12;  // 12–144
    } else {
        target = Math.floor(Math.random() * 11) + 2;    // 2–12
    }

    const correctEq    = makeCorrectEquation(op, target);
    const wrongEqs     = makeWrongEquations(op, target, correctEq, 3);

    const correctOption = { text: `${correctEq.a} ${correctEq.sym} ${correctEq.b}`, isCorrect: true };
    const wrongOptions  = wrongEqs.map(eq => ({ text: `${eq.a} ${eq.sym} ${eq.b}`, isCorrect: false }));

    return {
        target,
        options: [correctOption, ...wrongOptions].sort(() => 0.5 - Math.random())
    };
}


