/**
 * nextRound()
 * ────────────
 * EXPORTED — advances the game to the next round or ends the game if all
 * rounds are exhausted. Called by startGame() for round 0, then by
 * evaluateGuess() / evaluateMultiplayerRound() after a 2-second feedback pause.
 *
 * FLOW:
 *   1. Check if the game is over (curIdx >= maxRounds). If so → endGameSequence().
 *   2. Reset isProcessing so the next answer can register.
 *   3. Generate a fresh math problem via generateMathProblem().
 *   4. Update the round tag (#active-player) with round number or SD counter.
 *   5. Display the target number in the designated element.
 *   6. Render 4 MC buttons via _renderMCButtons().
 *   7. Start the countdown timer via _startRoundTimer().
 *
 * SUDDEN DEATH TIMER EXPIRY:
 *   When the timer hits 0 in Sudden Death, it calls evaluateGuess(false)
 *   to trigger the death sequence — same path as a wrong button tap.
 *
 * MULTIPLAYER NOTE:
 *   In multiplayer, nextRound() only runs on the HOST. Clients receive round
 *   state via Firebase and update their phone UI through the multiplayer listener.
 */
export function nextRound() {
    if (state.curIdx >= state.maxRounds) { endGameSequence(); return; }

    state.isProcessing  = false;
    _currentProblem     = generateMathProblem();
    state.currentCorrectAnswer = _currentProblem.target;

    const tag      = document.getElementById('active-player');
    const isDouble = !isSuddenDeath() && state.doubleRounds.includes(state.curIdx);

    // ── Round tag (top badge above the equation) ──
    if (isSuddenDeath()) {
        tag.innerText     = `💀 SUDDEN DEATH — Round ${state.sdRoundsAlive + 1}`;
        tag.style.color   = 'var(--fail)';
        tag.style.borderColor = 'var(--fail)';
    } else {
        tag.innerText     = `ROUND ${state.curIdx + 1}/${state.maxRounds}${isDouble ? ' — ⭐ 2X BONUS' : ''}`;
        tag.style.color   = isDouble ? '#f39c12' : 'var(--primary)';
        tag.style.borderColor = isDouble ? '#f39c12' : 'var(--primary)';
    }

    // ── Display the target number ──
    const targetEl = document.getElementById('math-target');
    if (targetEl) targetEl.innerText = _currentProblem.target;

    // ── Clear feedback from previous round ──
    const feedback = document.getElementById('feedback');
    if (feedback) feedback.innerHTML = '';

    // ── Render the four answer buttons ──
    _renderMCButtons(_currentProblem.options);

    // ── Start the countdown ──
    _startRoundTimer(state.timeLimit, () => {
        // Timer expired: treat as a wrong answer
        evaluateGuess(false, null);
    });

    // ── Sync to Firebase for multiplayer clients ──
    if (state.isMultiplayer && state.isHost) {
        db.ref(`rooms/${state.roomCode}/currentRound`).set({
            round:   state.curIdx,
            target:  _currentProblem.target,
            options: _currentProblem.options,
            isDouble
        });
    }
}


